-- 1. PEMBUATAN TABEL PROFILES (PENGGUNA / STAF)
CREATE TABLE IF NOT EXISTS public.profiles (
    id text PRIMARY KEY,
    email text UNIQUE NOT NULL,
    full_name text NOT NULL,
    role text NOT NULL,
    branch text NOT NULL,
    password text DEFAULT 'password123',
    created_at timestamp with time zone DEFAULT now()
);

-- 2. PEMBUATAN TABEL BACKCHARGES (TRANSAKSI DENDA / PENAGIHAN)
CREATE TABLE IF NOT EXISTS public.backcharges (
    id text PRIMARY KEY,
    category text NOT NULL,
    branch text NOT NULL,
    no_bak text DEFAULT '-',
    no_spk text DEFAULT '-',
    no_sap text DEFAULT '-',
    customer_name text NOT NULL,
    license_plate text DEFAULT '-',
    value bigint NOT NULL DEFAULT 0,
    status_sap text DEFAULT 'N/A',
    status_confirm text DEFAULT 'Belum Konfirmasi',
    status_handover text DEFAULT 'Pending',
    no_invoice text DEFAULT '-',
    status_payment text DEFAULT 'Belum Bayar',
    created_by text NOT NULL,
    created_at timestamp with time zone DEFAULT now(),
    updated_at timestamp with time zone DEFAULT now(),
    file_bak_url text,
    file_handover_aso_sales_url text,
    file_handover_sales_admin_url text
);

-- 3. PEMBUATAN TABEL ACTIVITY LOGS (LOG AKTIVITAS SISTEM)
CREATE TABLE IF NOT EXISTS public.activity_logs (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    timestamp timestamp with time zone DEFAULT now(),
    transaction_id text NOT NULL,
    performed_by text NOT NULL,
    action_description text NOT NULL
);

-- 4. NONAKTIFKAN ROW LEVEL SECURITY (RLS) UNTUK KEPERLUAN INTERNAL CEPAT
-- Jika Anda ingin mengaktifkan RLS, buatlah policy SELECT/INSERT/UPDATE terlebih dahulu.
-- Menonaktifkan RLS memastikan aplikasi web Anda dapat langsung terhubung & sinkron.
ALTER TABLE public.profiles DISABLE ROW LEVEL SECURITY;
ALTER TABLE public.backcharges DISABLE ROW LEVEL SECURITY;
ALTER TABLE public.activity_logs DISABLE ROW LEVEL SECURITY;

