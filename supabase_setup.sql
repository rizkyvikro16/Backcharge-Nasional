-- 6. [OPSIONAL] SINKRONISASI SUPABASE AUTH DENGAN PUBLIC.PROFILES
-- Script di bawah ini akan otomatis membuat profil di tabel 'profiles' ketika pengguna mendaftar lewat Supabase Auth.
-- Jalankan ini agar setiap kali user login lewat email auth, mereka punya entri profile secara otomatis.
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger AS $$
BEGIN
  INSERT INTO public.profiles (id, email, full_name, role, branch, password, created_at)
  VALUES (
    new.id,
    new.email,
    COALESCE(new.raw_user_meta_data->>'full_name', split_part(new.email, '@', 1)),
    'ASO / Staff',
    'Jakarta',
    'password123',
    now()
  )
  ON CONFLICT (email) DO UPDATE
  SET id = EXCLUDED.id; -- Menghubungkan ID baru dari Auth
  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Memicu fungsi handle_new_user setiap kali ada baris baru di tabel auth.users
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

-- 7. AKTIFKAN REALTME DARI DATABASE SUPABASE
-- Script ini akan menghapus dan membuat ulang publikasi 'supabase_realtime' untuk mendeteksi
-- insert, update, dan delete secara instan agar tombol lonceng Notifikasi Real-Time di aplikasi menyala otomatis.
BEGIN;
  DROP PUBLICATION IF EXISTS supabase_realtime CASCADE;
  CREATE PUBLICATION supabase_realtime FOR TABLE public.backcharges, public.profiles, public.activity_logs;
COMMIT;

